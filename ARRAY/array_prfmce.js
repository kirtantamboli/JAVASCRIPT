// push() method
let Animals = ["Tiger", "Giraffe", "Horse", "Deer"];
Animals.push("Zebra");
console.log(Animals);

// pop() method
let Animals2 = ["Tiger", "Giraffe", "Horse", "Deer"];
Animals2.pop();
console.log(Animals2);

// shift() method
let Animals3 = ["Tiger", "Giraffe", "Horse", "Deer"];
Animals3.shift();
console.log(Animals3);


